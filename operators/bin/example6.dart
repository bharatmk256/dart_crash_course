void main(List<String> args) {
  // print('Hello' + 2);
  var myAge = 20;
  print(myAge = 30); // myAge = 30
  print(myAge ~/= 2); // myAge = 15
  print(myAge *= 2); // myAge = 30
  print(myAge += 4); // myAge = 34
  print(myAge &= 2); // myAge = 2
  print(myAge |= 6); // myAge = 6
  print(myAge ^= 10); // myAge = 12
  print(myAge -= 10); // myAge = 2
}
